//读取SPI FLASH  
//在指定地址开始读取指定长度的数据
//pBuffer:数据存储区
//ReadAddr:开始读取的地址(24bit)
//NumByteToRead:要读取的字节数(最大65535)
void SPI_Flash_Read(u8* pBuffer,u32 ReadAddr,u16 NumByteToRead)   
{ 
 	u16 i;    		
  OS_ERR err;	
  OSMutexPend(&MOTO_SEM,0,OS_OPT_PEND_BLOCKING,0,&err); //请求信号量
	SPEED_FLASH;
	FLASH_EN;                            //使能器件  
  
	FLASH_ReadWriteByte(W25X_ReadData);         //发送读取命令   
	FLASH_ReadWriteByte((u8)((ReadAddr)>>16));  //发送24bit地址    
	FLASH_ReadWriteByte((u8)((ReadAddr)>>8));   
	FLASH_ReadWriteByte((u8)ReadAddr);
	for(i=0;i<NumByteToRead;i++)
	{
			pBuffer[i]=FLASH_ReadWriteByte(0XFF);   //循环读数  
	}
	FLASH_DS;                            //取消片选   
	SPEED_FPGA;
	OSMutexPost (&MOTO_SEM,OS_OPT_POST_NO_SCHED,&err);
}  
