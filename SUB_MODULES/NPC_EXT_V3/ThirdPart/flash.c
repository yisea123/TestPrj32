//擦除整个芯片
//整片擦除时间:
//W25X16:25s 
//W25X32:40s 
//W25X64:40s 
//等待时间超长...
void SPI_Flash_Erase_Chip(void)   
{                                             
    SPI_FLASH_Write_Enable();                  //SET WEL 
    SPI_Flash_Wait_Busy();   
  	FLASH_EN;                            //使能器件   
    FLASH_ReadWriteByte(W25X_ChipErase);        //发送片擦除命令  
	FLASH_DS;                            //取消片选     	      
	SPI_Flash_Wait_Busy();   				   //等待芯片擦除结束
}   
//擦除一个扇区
//Dst_Addr:扇区地址 0~511 for w25x16
//擦除一个山区的最少时间:150ms
void SPI_Flash_Erase_Sector(u32 Dst_Addr)   
{   
	Dst_Addr*=4096;
    SPI_FLASH_Write_Enable();                  //SET WEL 	 
    SPI_Flash_Wait_Busy();   
  	FLASH_EN;                            //使能器件   
    FLASH_ReadWriteByte(W25X_SectorErase);      //发送扇区擦除指令 
    FLASH_ReadWriteByte((u8)((Dst_Addr)>>16));  //发送24bit地址    
    FLASH_ReadWriteByte((u8)((Dst_Addr)>>8));   
    FLASH_ReadWriteByte((u8)Dst_Addr);  
	FLASH_DS;                            //取消片选     	      
    SPI_Flash_Wait_Busy();   				   //等待擦除完成
}  
//等待空闲
void SPI_Flash_Wait_Busy(void)   
{   
	while ((SPI_Flash_ReadSR()&0x01)==0x01);   // 等待BUSY位清空
}  
//进入掉电模式
void SPI_Flash_PowerDown(void)   
{ 
  	FLASH_EN;                            //使能器件   
    FLASH_ReadWriteByte(W25X_PowerDown);        //发送掉电命令  
	  FLASH_DS;                            //取消片选     	      
    Delay(6);                               //等待TPD  
}   
//唤醒
void SPI_Flash_WAKEUP(void)   
{  
  	FLASH_EN;                            //使能器件   
    FLASH_ReadWriteByte(W25X_ReleasePowerDown);   //  send W25X_PowerDown command 0xAB    
	  FLASH_DS;                            //取消片选     	      
    Delay(6);                              //等待TRES1
}   



