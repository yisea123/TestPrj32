//读取SPI_FLASH的状态寄存器
//BIT7  6   5   4   3   2   1   0
//SPR   RV  TB BP2 BP1 BP0 WEL BUSY
//SPR:默认0,状态寄存器保护位,配合WP使用
//TB,BP2,BP1,BP0:FLASH区域写保护设置
//WEL:写使能锁定
//BUSY:忙标记位(1,忙;0,空闲)
//默认:0x00
u8 SPI_Flash_ReadSR(void)   
{  
	u8 byte=0;   
	FLASH_EN;                            //使能器件   
	FLASH_ReadWriteByte(W25X_ReadStatusReg);    //发送读取状态寄存器命令    
	byte=FLASH_ReadWriteByte(0Xff);             //读取一个字节  
	FLASH_DS;                            //取消片选     
	return byte;   
} 
//写SPI_FLASH状态寄存器
//只有SPR,TB,BP2,BP1,BP0(bit 7,5,4,3,2)可以写!!!
void SPI_FLASH_Write_SR(u8 sr)   
{   
	FLASH_EN;                            //使能器件   
	FLASH_ReadWriteByte(W25X_WriteStatusReg);   //发送写取状态寄存器命令    
	FLASH_ReadWriteByte(sr);               //写入一个字节  
	FLASH_DS;                            //取消片选     	      
}   
